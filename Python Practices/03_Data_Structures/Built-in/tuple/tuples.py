# ============================= What is tuple ==============================
# A tuple is an ordered, immutable sequence in Python.

# ============================= basic tuple ================================
# How to create tuples: (), (1,), (1,2,3), tuple([1,2,3])

# ============================= tuple operations ===========================
# Concatenation (+), Repetition (*), Membership (in), Indexing, Slicing

# ============================= tuple iteration ============================
# Loop through a tuple using for

# ============================= tuple methods ==============================
# Only two built-in methods: count(), index()

# ============================= tuple count ================================
# tuple.count(value) – number of occurrences

# ============================= tuple index ================================
# tuple.index(value) – first index of value

# ============================= tuple unpacking ============================
# a, b, c = (1, 2, 3) – assigning multiple variables

# ============================= nested tuple ===============================
# Tuples inside tuples ((1,2), (3,4))

# ============================= tuple immutability =========================
# Cannot change items, but can hold mutable objects (like list)

# ============================= tuple as dictionary key ====================
# Tuples can be used as dictionary keys (if containing only immutable types)
